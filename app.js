async function connectWallet() {
  if (window.ethereum) {
    provider = new ethers.providers.Web3Provider(window.ethereum);
    await provider.send("eth_requestAccounts", []);
    signer = provider.getSigner();
    const address = await signer.getAddress();
    document.getElementById("walletAddress").textContent = address;

    tokenContract = new ethers.Contract(tokenAddress, tokenAbi, signer);
    vendorContract = new ethers.Contract(vendorAddress, vendorAbi, signer);

    updateBalance();
  } else {
    alert("Hãy cài đặt MetaMask!");
  }
}

async function updateBalance() {
  const address = await signer.getAddress();
  const balance = await tokenContract.balanceOf(address);
  document.getElementById("tokenBalance").textContent = ethers.utils.formatUnits(balance, 18);
}

async function buyTokens() {
  const ethAmount = document.getElementById("ethAmount").value;
  const tx = await vendorContract.buyTokens({ value: ethers.utils.parseEther(ethAmount) });
  await tx.wait();
  updateBalance();
}

async function sellTokens() {
  const tokenAmount = document.getElementById("tokenAmount").value;
  const amount = ethers.utils.parseUnits(tokenAmount, 18);
  
  const approvalTx = await tokenContract.approve(vendorAddress, amount);
  await approvalTx.wait();

  const tx = await vendorContract.sellTokens(amount);
  await tx.wait();

  updateBalance();
}
